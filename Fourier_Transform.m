fs=600;
figure;
t1 = 0:1/fs:2*pi;
y1 = sin(50*2*pi*t1) + sin(100*2*pi*t1) + sin(150*2*pi*t1) + sin(180*2*pi*t1);
Y1 = fft(y1);
L1 = length(y1);
f1 = (0:L1-1)*(fs/L1);
amplitude1 = abs(Y1);
plot(f1, amplitude1);
grid on;
title("Fourier Transform(0 to 2*pi)");
xlabel('Frequency (Hz)');
ylabel('Amplitude');

figure;
t2 = -pi:1/fs:pi;
y2 = sin(50*2*pi*t2) + sin(100*2*pi*t2) + sin(150*2*pi*t2) + sin(180*2*pi*t2);
Y2 = fft(y2);
L2 = length(y2);
f2 = [(1:L2/2-1)*(fs/L2) , (-L2/2:0)*(fs/L2)];
amplitude2 = abs(Y2);
plot(f2, amplitude2);
grid on;
title("Fourier Transform(-pi to pi)");
xlabel('Frequency (Hz)');
ylabel('Amplitude');